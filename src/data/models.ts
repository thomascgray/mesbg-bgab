// if a model has "profiles" it means that the model
// is actually represented by more than 1 physical model
// e.g those elf brothers
// in this case, each profile should have a key and a name
// if the profile has a "stats", it overwrites the main model's "stats"
// otherwise the main models stats are used for all profiles
// same for wargear and wargear options
export interface iModelProfile {
  key: string;
  name?: string;
  stats?: {
    Mv: number;
    F1: number;
    F2: number;
    S: number;
    D: number;
    A: number;
    W: number;
    C: number;
    Mi?: number;
    Wi?: number;
    Fa?: number;
  };
  effectiveQuantity?: number;
  effectiveBowQuantity?: number; // warg marauders have 2 bows but it only counts as 1
  wargear?: iOption[];
}

export interface iModel {
  heroLevel?: eHeroLevel;
  key: string;
  cost: number;
  effectiveQuantity?: number; // sometimes a Model actually consists of multiple real-world "models" e.g dwarf vault teams
  effectiveBowQuantity?: number; // warg marauders have 2 bows but it only counts as 1
  stats?: {
    Mv: number;
    F1: number;
    F2: number;
    S: number;
    D: number;
    A: number;
    W: number;
    C: number;
    Mi?: number;
    Wi?: number;
    Fa?: number;
  };
  maxCost?: number;
  maxMi?: number;
  maxWi?: number;
  maxFa?: number;
  profiles?: iModelProfile[];
  name: string;
  wargear?: iOption[]; // the models starting options
  wargearOptions?: iOption[]; // any options that the model can optionally choose to take
  wargearUpgrades?: iOption[]; // upgrades that the unit can take
  wargearFromChoices?: iOption[]; // options that the model has taken from choices e.g they NEED to have one of the choices
  wargearFromUpgrades?: iOption[]; // options that the model has taken that it is allowed via upgrades
  allowPurchaseMi?: boolean; // can this hero purchase increases to its Mi, Wi and Fa?
  allowPurchaseWi?: boolean; // can this hero purchase increases to its Mi, Wi and Fa?
  allowPurchaseFa?: boolean; // can this hero purchase increases to its Mi, Wi and Fa?

  // functions like army may fields, allows heroes to field armies from other lists
  // if a hero has a mayField or a unit that it is already allowed to field, the heros
  // mayField OVERWRITES the "army" model
  // a models mayField like this appears as their warband
  mayField?: iModel[];

  // units can also specify units that get added to the roster
  // at an ARMY level. This is useful for when having a hero in an army allows
  // other random heroes e.g Balin may field Gimli (from the Fellowship)
  armyMayField?: iModel[];
}

export interface ISiegeEngine {
  isVeteranUpgradedToCaptain: boolean;
  stats: {
    S: number;
    D: number;
    W: number;
  };
}

// a model that is in an army gets a few extra fields
export interface iModelInArmy extends iModel {
  id: string; // a unique ID
  equippedWargear: iOption[]; // wargear the user has equipped
  quantity: number; // how many of the model with this wargear in this "squad". squads don't really exist, because models have quantitys.
}

// a hero model that HAS been added to an army.
// when they get added to an army, they received a warband array.
export interface iHeroModelInArmy extends iModelInArmy {
  warband: iModelInArmy[];
}

